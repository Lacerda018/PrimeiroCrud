<?php

$nome = "joao rangel";

//strtoupper() transforma todas as letras em maiusculo
echo strtoupper($nome)."<br>";

//strtolower() transforma todas as letras em minusculo
echo strtolower($nome)."<br>";

//ucwords() transforma a primeira letra de cada palavra em maiuscula
echo ucwords($nome)."<br>";

//ucfirst() transforma a primeira letra em maiuscula
echo ucfirst($nome)."<br>";

?>